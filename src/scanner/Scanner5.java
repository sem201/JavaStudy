public class Scanner5 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);

        while(true){
            String num1 = scanner.nextLine();
            String num2 = scanner.nextLine();
            if(num1.equals("0") && num2.equals("0")){
                break;
            }

        }

    }
}
